'''
Python = Linguagem de progamação
Tipo de linguagem = Dinamica/Forte
str -> string -> texto
String são texto que estão dentrodas de aspas

'''
print(123)
# Aspas siples
print('André Lucas')
# Aspas duplas
print("André Lucas")
# Escape --> \" Esse comando faz com que a aspas no meio da String seja entendida como tal, e não como o fachamento do comando.
print("André \"Lucas\"") 
# r --> Esse comando faz com que o comando de escape apareca com string
print(r"André \"Lucas\"") 

# Uma solução fácil é usar aspas invertidada. Por ex:
print('"André Lucas"')
print("'André Lucas'")

