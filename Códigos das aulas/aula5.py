# Tipo da dade bool (boolean) 
# Só existe duas resposta possiveis:
# sim (True) ou não (False) --- Os dados são com a primeira letra maiúscula
# Existe vários operadores para "questionar".
# Destre eles ==, que é um operador lógico que
# questiona se um valor é igual a outro.
print(100 == 100) # Sim -> True (verdadeiro)
print(10 == 11) # Não -> False (falso)
print(type(10 == 11)) # Esse expresão mostra o tipo do dado, que no caso é bool